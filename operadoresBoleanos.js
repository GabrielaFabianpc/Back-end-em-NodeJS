if (true) {
  console.log("sempre aparecerei");
}

if (false) {
  console.log("não aparecerei");
}

//expressões com ou sempre retornam verdadeiro, a menos que TODAS as expressões sejam falsas
console.log(true || true);
console.log(true || false);
console.log(false || false);

//expressões com e sempre retornam falso, a menos que TODAS as expressões sejam verdadeiras
console.log(true && true);
console.log(true && false);
console.log(false && true);
